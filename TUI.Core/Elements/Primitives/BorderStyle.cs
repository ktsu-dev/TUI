namespace ktsu.TUI.Core.Elements.Primitives;

/// <summary>
/// Defines border styles
/// </summary>
public enum BorderStyle
{
	/// <summary>
	/// No border
	/// </summary>
	None,

	/// <summary>
	/// Single line border
	/// </summary>
	SingleLine,

	/// <summary>
	/// Double line border
	/// </summary>
	DoubleLine,

	/// <summary>
	/// Rounded corner border
	/// </summary>
	Rounded,

	/// <summary>
	/// Thick line border
	/// </summary>
	Thick,

	/// <summary>
	/// ASCII compatible border
	/// </summary>
	Ascii
}
